// Componente Padre
import React, { Component } from 'react'; // Importamos React y Component
import Producto from './Producto'; // Importamos el componente hijo

class ListaProductos extends Component {
    constructor(props) {
        super(props); // Llamamos al constructor de la clase padre
        this.state = {
            productos: [ // Definimos un array de productos en el estado
                { id: 1, nombre: 'Producto 1', precio: 100 },
                { id: 2, nombre: 'Producto 2', precio: 200 },
                { id: 3, nombre: 'Producto 3', precio: 300 }
            ],
            carrito: [] // Inicializamos el carrito como un array vacío
        };
    }

    // Método para agregar un producto al carrito
    agregarAlCarrito = (producto) => {
        this.setState((prevState) => ({
            carrito: [...prevState.carrito, producto] // Agregamos el producto al carrito existente
        }));
    }

    render() {
        return (
            <div>
                <h1>Lista de Productos</h1>
                <ul>
                    {/* Usamos map() para iterar sobre los productos y renderizar cada uno */}
                    {this.state.productos.map((producto) => (
                        <Producto 
                            key={producto.id} // Asignamos una clave única para cada producto
                            producto={producto} // Pasamos el producto como prop al componente hijo
                            agregarAlCarrito={this.agregarAlCarrito} // Pasamos la función para agregar al carrito
                        />
                    ))}
                </ul>
                <h2>Carrito</h2>
                <ul>
                    {/* Listamos los productos en el carrito */}
                    {this.state.carrito.map((item, index) => (
                        <li key={index}>{item.nombre} - ${item.precio}</li> // Mostramos nombre y precio del producto
                    ))}
                </ul>
            </div>
        );
    }
}

export default ListaProductos; // Exportamos el componente para usarlo en otros archivos