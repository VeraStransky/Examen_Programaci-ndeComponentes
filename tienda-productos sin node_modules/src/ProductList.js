import React from 'react'; // Importamos React
import ProductItem from './ProductItem'; // Importamos el componente ProductItem

const ProductList = ({ products, deleteProduct }) => {
    return (
        <ul>
            {products.map((product) => (
                <ProductItem 
                    key={product.id} // Usamos el ID como clave única para cada producto
                    product={product} 
                    deleteProduct={deleteProduct} // Pasamos la función para eliminar productos
                />
            ))}
        </ul>
    );
};

export default ProductList; // Exportamos el componente para usarlo en otros archivos