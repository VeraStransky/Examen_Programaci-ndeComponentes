//Componente Hijo
import React from 'react'; // Importamos React

const Producto = ({ producto, agregarAlCarrito }) => {
    const handleClick = () => {
        agregarAlCarrito(producto); // Llama a la función para agregar el producto al carrito cuando se hace clic
    };

    return (
        <li>
            {producto.nombre} - ${producto.precio} {/* Mostramos el nombre y precio del producto */}
            <button onClick={handleClick}>Agregar al Carrito</button> {/* Botón que llama a handleClick */}
        </li>
    );
};

export default Producto; // Exportamos el componente para usarlo en otros archivos